function changenumon()
{

    document.getElementById("numon").style.display = "none";
    document.getElementById("num2").style.display = "table-row";
    document.getElementById("numon").style.display = "none";
    document.getElementById("numoff").style.display = "block";
}

function changenumoff()
{
    document.getElementById("numoff").style.display = "block";
    document.getElementById("num2").style.display = "none";
    document.getElementById("numoff").style.display = "none";
    document.getElementById("numon").style.display = "block";
}

