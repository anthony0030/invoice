function changecoloron()
{

    document.getElementById("logo").src="assets/img/logo/logo.jpg";
    document.getElementById("color").className = "nodisplay";
    document.getElementById("nocolor").className = "display";
}

function changecoloroff()
{
    document.getElementById("logo").src="assets/img/logo/logoblack.jpg";
    document.getElementById("nocolor").className = "nodisplay";
    document.getElementById("color").className = "display";
}

